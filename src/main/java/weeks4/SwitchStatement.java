package weeks4;

public class SwitchStatement {

    public static void main(String[] args) {

        int someCase = 4;

        switch (someCase) {
            case 1:
                System.out.println(" 1번 케이스 ");

            case 2:
                System.out.println(" 2번 케이스 ");

            case 3:
                System.out.println(" 3번 케이스 ");

            case 4:
                System.out.println(" 4번 케이스 ");

            case 5:
                System.out.println(" 5번 케이스 ");

        }
    }
}
